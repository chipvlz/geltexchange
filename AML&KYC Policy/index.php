discovered due to them becoming of material values.<br>
<br>
The government of every country fights against money laundering and terroristic
financial operations because it is necessary to prevent the entry of criminal
funds into the economy. Financial and commercial institutions are the most
easily accessible for illegal processes of terroristic and criminal
organizations. Especially digital currency services are vulnerable. Thus, SafePayTm
company takes an active part in the prevention of such criminal activity in the
whole world.<br>
<br>
For these goals we do the following actions:<br>
1. We conduct the process of verification of the client’s identity and personal
information and monitor all transactions made by customers.<br>
2. Our team traces all suspicious operations of users and also those made under
irregular circumstances.<br>
3. If our company suspects the illegal activity or money laundering, it has the
full right to request additional verification or cancel such transaction at any
step.<br>
4. According to the international law, SafePayTm is not obligated to give
customer any notifications about suspicion in the user’s criminal actions.<br>
5. Such company’s actions are based on the AML FATF references.<br>
<br>
To prevent the suspicious operations and verify the client’s identity, the team
of our company has the right to update its electronic system on the regular
basis according to new regulations (if those appear). We also arrange various
trainings for our staff for improving the methods of struggle against money
laundering.<o:p></o:p></span></p>

